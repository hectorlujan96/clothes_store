    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        
        
        if (txtCantidad.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo de cantidad sin valor");
        } else {
        
        int a =  (int) (Math.random()*200 + 300);
        int b = Integer.parseInt(txtCantidad.getText());
        
        
        o[0] = txtProducto.getText();
        o[1] = a;
        o[2] = b;
        o[3] =  a * b;
        
        modelo.addRow(o);
        
        int subtotal = 0; 
        for (int i = 0; i < tablaVendidos.getRowCount(); i++) {
        subtotal += (int) tablaVendidos.getValueAt(i, 3); //La columna 3 es la de total
        }
        lblTotal.setText(String.valueOf(subtotal));
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

